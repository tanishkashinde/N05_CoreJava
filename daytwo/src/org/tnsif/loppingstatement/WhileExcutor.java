package org.tnsif.loppingstatement;
import java.util.Scanner;
public class WhileExcutor {

	public static void main(String[] args) { 
	Scanner s=new Scanner(System.in);
	int n=s.nextInt();
	int i=1;
	
	while(n>=i)
	{
		System.out.println(n+" ");
		n--;
		
	}
		
		
		
		
		
		
		
		

	}

}
